package com.company.java0912;

public class Q20250912Test_4 {
	public static void main(String[] args) {
		// 변수

		for (int i = 5; i >= 1; i--) {
			System.out.print(i + " ");
		}
		System.out.println();

		int j = 5;
		while (j >= 1) {
			System.out.print(j + " ");
			j--;
		}
		System.out.println();

		int k = 5;
		do {
			System.out.print(k + " ");
			k--;
		} while (k >= 1);

	}

}
