package com.company.java0905;

import java.util.Scanner;

public class Repeat010_ex {
	public static void main(String[] args) {
		//변수
		int num;
		Scanner scanner = new Scanner(System.in);
		
		System.out.println((int)'0');
		
		//입력
		System.out.println("숫자한개를 입력하시오>");
		num = scanner.nextInt();
		//처리
		if (num == 1) {System.out.println("one");}
		else if (num == 2) {System.out.println("two");}
		else if (num == 3) {System.out.println("three");}
		else {System.out.println("1,2,3,이 아니다");}
		}
		
		
		
		//출력
		
	}


