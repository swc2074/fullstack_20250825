package com.company.java005_ex;

public class ForEx003 {
	public static void main(String[] args) {
		
		// 변수
		int i =0, hap=0;
		System.out.println(" 1~ 10까지의 합을 구하시오");
		
		 
		for (i = 1; i<=10; i++){hap = hap+i;
		

		
		System.out.printf((i==1 ? " ": "+") + i);}
		System.out.println("=" + hap);
		
		// {반복}  {변수} for (시작 ; 종료 ; 변화)
		
	}

}
