package com.company.java005_ex;

public class Repeat012_3 {
	public static void main(String[] args) {
			
		for (int a = 1; a <= 5; a++) {System.out.print(a + "\t");}
		System.out.println();
		for (int a = 5; a >= 1; a--) {System.out.print(a + "\t");}
		System.out.println();
		for (int a = 1; a <= 3; a++) {System.out.print("JAVA" + a + "\t");}
		}
		
		
	

}
